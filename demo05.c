extern const uint8_t demo05_bytecode[];

void main() {
  sbrk(&_heap, 4096);  // Register 4KB starting at _heap

  SMS_VRAMmemset(0x0000, 0x00, 16384);
  load_tiles(standard_font, 0, 255, 1);
  SMS_loadBGPalette(pal1);
  SMS_loadSpritePalette(pal2);
  SMS_useFirstHalfTilesforSprites(0);

  // Load sprites for player and enemy
  SMS_loadTiles(player_tile, 257, 32);
  SMS_loadTiles(enemy_tile, 258, 32);
  SMS_loadTiles(bullet_tile, 259, 32);
  SMS_loadTiles(explosion_tile, 260, 32);

  SMS_displayOn();

  printf("game demo\r");

  mrbz_val v;
  mrbz_vm vm;
  mrbz_vm_run(&vm, &v, demo05_bytecode);
}
